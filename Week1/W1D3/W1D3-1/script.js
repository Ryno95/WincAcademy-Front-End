// Objects
let person = {
    name: 'Ryno',
    age: 24
}; // {} <--- Object litteral

// Dot Notation
person.name = 'Janus'
person.age = 31

// Bracket Notation
let selection = 'name'
person[selection] = 'Aweee brade!';
person['age'] = 62

console.log(person);


